package iut.unilim.fr.marsRover;

public class Planete {
	private int largeur;
	private int longueur;

	public Planete(int largeur, int longueur) {
		this.largeur = largeur;
		this.longueur = longueur;
	}

	public int getLargeur() {
		return largeur;
	}

	public int getLongueur() {
		return longueur;
	}


}
